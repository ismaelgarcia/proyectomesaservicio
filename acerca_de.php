                <h4>Antecedentes</h4>
                <div style="margin-left: 20px">
                    <h5>2011.</h5>
                    <p class="lead">Mediante decreto del Ejecutivo número 438, publicado en el Periódico Oficial del Estado en fecha 15 de marzo de 2011, se divide la Secretaría de Desarrollo Urbano y Medio Ambiente para dar lugar a la Secretaría de Desarrollo Urbano del Estado de Quintana Roo, Dependencia del Ejecutivo Estatal a la que le corresponde conducir las políticas públicas y programas sectoriales en materia de desarrollo urbano y ordenamiento territorial.</p>
              
                    <h5>2013.</h5>
                    <p class="lead">El 8 de marzo del año 2013 se publica en el Periódico Oficial del Estado el Decreto número 247, a través del cual la Secretaría de Desarrollo Urbano cambia de nombre para denominarse Secretaría de Desarrollo Urbano y Vivienda del Estado de Quintana Roo, estableciéndose como la Dependencia estatal a la que le corresponde conducir las políticas públicas y programas sectoriales en materia de desarrollo urbano, vivienda y ordenamiento territorial.</p>
                    <p class="lead">Posteriormente, a través de la publicación del Decreto número 306 en el Periódico Oficial del Estado el día 19 de agosto de ese mismo año, se extinguió el Instituto de Fomento a la Vivienda y Regularización de la Propiedad del Estado de Quintana Roo (INFOVIR), incorporándose a la Secretaría de Desarrollo Urbano y Vivienda todo el recurso humano, patrimonio y las atribuciones que correspondía a dicha entidad.</p>
        
                    <h5>2017.</h5>
                    <p class="lead">El 19 de julio de 2017 se publicó el Decreto número 083 en el Periódico Oficial del Estado, a través del cual la Secretaría de Desarrollo Urbano y Vivienda modifica su nombre a Secretaría de Desarrollo Territorial Urbano Sustentable, siendo la dependencia coordinadora de las políticas y programas sectoriales del ordenamiento territorial y el desarrollo urbano en el estado, que regula el establecimiento equilibrado de los asentamientos humanos, con especial énfasis en la creación, regulación y edificación de viviendas.</p>
        
                    <h5>2019.</h5>
                    <p class="lead">El 16 de agosto de 2018 se publicó el decreto 194, se aprobó la Reforma Integral Urbana a través del cual se expidieron las nuevas leyes de Asentamientos Humanos, Ordenamiento Territorial y Desarrollo Urbano y la de Acciones Urbanísticas, ambas del Estado, igualmente se reformaron y adicionaron diversas disposiciones de la Ley de Vivienda del Estado de Quintana Roo y se adicionó un cuarto párrafo al artículo 4 de la Ley de Propiedad en Condominio de Inmuebles del Estado, normatividad que otorgó a la Secretaría de Desarrollo Territorial Urbano Sustentable facultades más específicas en materia de desarrollo urbano, ordenamiento territorial y vivienda.</p>
